def main():
    # Your code here
    print("Hello from main!")

if __name__ == "__main__":
    main()

# * When you run a program, OS loads your program into memory
# * OS then call for a startup routine provided by the language runtime
# * Startup routine looks for main() -  this is defined as starting point
# * It is a standard convention

# *  Every python file(module) has a built in variable called __name__
# * If file is run directly -> then python sets __name__ == "__main__"
# * If file is imported into another file (like import myScript) then  __name__ = "<module_name>"






